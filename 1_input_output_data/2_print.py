# Каждый print() выводит данные на новой строке.

print('Hello world')

print('Hello') # По умолчанию \n
print('world')

print('Hello', end=' ') # Вопрос 3: какие спец. символы ещё существуют? \t \r
print('world')

#Исторически \n использовался для перемещения каретки вниз, в то время как \r использовался
#для перемещения каретки обратно в левую часть страницы.